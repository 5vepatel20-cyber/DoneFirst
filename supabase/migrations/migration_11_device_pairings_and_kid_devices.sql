-- ===========================================================================
-- kid_devices_with_child view
-- ===========================================================================
-- Joins kid_devices with children so the parent app can show
-- "Aarav's Pixel 8" without N+1 queries. Status is derived from
-- last_seen_at and revoked_at. Defined here for completeness but
-- also referenced / re-CREATEd in migration_13.
-- ===========================================================================

DROP VIEW IF EXISTS kid_devices_with_child;
CREATE VIEW kid_devices_with_child AS
  SELECT
    kd.id,
    kd.family_id,
    kd.child_id,
    c.name AS child_display_name,
    kd.device_name,
    kd.paired_at,
    kd.last_seen_at,
    kd.revoked_at,
    CASE
      WHEN kd.revoked_at IS NOT NULL THEN 'revoked'
      WHEN kd.last_seen_at IS NULL THEN 'unknown'
      WHEN kd.last_seen_at > now() - interval '90 seconds' THEN 'online'
      WHEN kd.last_seen_at > now() - interval '24 hours' THEN 'recent'
      ELSE 'stale'
    END AS status
  FROM kid_devices kd
  LEFT JOIN children c ON c.id = kd.child_id;
