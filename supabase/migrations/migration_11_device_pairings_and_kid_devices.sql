-- ===========================================================================
-- device_pairings
-- ===========================================================================
-- Stores 6-digit pairing codes generated by the parent app.
-- The PRIMARY KEY is `code` (text), not a UUID — the code itself
-- is the natural key and is how the claim-pairing Edge Function
-- looks up rows.
-- ===========================================================================

CREATE TABLE IF NOT EXISTS device_pairings (
  code            TEXT PRIMARY KEY,
  family_id       UUID NOT NULL REFERENCES families(id) ON DELETE CASCADE,
  child_id        UUID NOT NULL REFERENCES children(id) ON DELETE CASCADE,
  created_by      UUID NOT NULL REFERENCES auth.users(id),
  expires_at      TIMESTAMPTZ NOT NULL,
  claimed_at      TIMESTAMPTZ,
  claimed_by_device UUID REFERENCES kid_devices(id)
);

CREATE INDEX IF NOT EXISTS device_pairings_code_idx
  ON device_pairings (code);

CREATE INDEX IF NOT EXISTS device_pairings_family_id_idx
  ON device_pairings (family_id);

CREATE INDEX IF NOT EXISTS device_pairings_expires_at_idx
  ON device_pairings (expires_at);

ALTER TABLE device_pairings ENABLE ROW LEVEL SECURITY;

-- Original fine-grained policies from migration 11:
DROP POLICY IF EXISTS "Parents can create pairing codes" ON device_pairings;
CREATE POLICY "Parents can create pairing codes"
  ON device_pairings FOR INSERT
  TO authenticated
  WITH CHECK (
    created_by = auth.uid()
    AND EXISTS (
      SELECT 1 FROM parents p
      WHERE p.family_id = device_pairings.family_id
      AND p.id = auth.uid()
    )
  );

DROP POLICY IF EXISTS "Parents can see their pairing codes" ON device_pairings;
CREATE POLICY "Parents can see their pairing codes"
  ON device_pairings FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM parents p
      WHERE p.family_id = device_pairings.family_id
      AND p.id = auth.uid()
    )
  );

DROP POLICY IF EXISTS "Parents can delete their pairing codes" ON device_pairings;
CREATE POLICY "Parents can delete their pairing codes"
  ON device_pairings FOR DELETE
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM parents p
      WHERE p.family_id = device_pairings.family_id
      AND p.id = auth.uid()
    )
  );

-- Service-role access (Edge Functions use service_role for writes):
DROP POLICY IF EXISTS "Service role manages device_pairings writes" ON device_pairings;
CREATE POLICY "Service role manages device_pairings writes"
  ON device_pairings FOR ALL
  TO public
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

-- ===========================================================================
-- kid_devices
-- ===========================================================================
-- One row per physical device paired to a kid. Created by the
-- claim-pairing Edge Function (service_role). Parents can read
-- and update (revoke) but not insert — inserts only via Edge Fn.
-- ===========================================================================

CREATE TABLE IF NOT EXISTS kid_devices (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  child_id    UUID NOT NULL REFERENCES children(id) ON DELETE CASCADE,
  family_id   UUID NOT NULL REFERENCES families(id) ON DELETE CASCADE,
  device_name TEXT,
  paired_at   TIMESTAMPTZ DEFAULT now(),
  last_seen_at TIMESTAMPTZ,
  revoked_at  TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS kid_devices_family_id_idx
  ON kid_devices (family_id);

CREATE INDEX IF NOT EXISTS kid_devices_child_id_idx
  ON kid_devices (child_id);

ALTER TABLE kid_devices ENABLE ROW LEVEL SECURITY;

